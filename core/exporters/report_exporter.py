class ExcelExporter:
    """Excel 格式导出器"""
    
    def __init__(self):
        self.header_font = Font(bold=True, color="FFFFFF")
        self.header_fill = PatternFill(start_color="366092", end_color="366092", fill_type="solid")
        self.alignment = Alignment(horizontal="left", vertical="center")
    
    def export(self, data: Dict, output_path: str) -> bool:
        """导出为 Excel 多 Sheet"""
        try:
            wb = openpyxl.Workbook()
            wb.remove(wb.active)
            
            self._create_summary_sheet(wb, data)
            self._create_apis_sheet(wb, data)
            self._create_vulns_sheet(wb, data)
            self._create_sensitive_sheet(wb, data)
            
            wb.save(output_path)
            return True
        except Exception as e:
            logger.error(f"Excel export error: {e}")
            return False
    
    def _create_summary_sheet(self, wb, data: Dict):
        """创建摘要 Sheet"""
        ws = wb.create_sheet("Summary")
        
        summary = data.get('summary', {})
        
        rows = [
            ("Target", data.get('target_url', '')),
            ("Scan Time", data.get('start_time', '')),
            ("Duration", f"{data.get('duration', 0):.2f}s"),
            ("", ""),
            ("API Statistics", ""),
            ("Total APIs", summary.get('total_apis', 0)),
            ("Alive APIs", summary.get('alive_apis', 0)),
            ("High Value APIs", summary.get('high_value_apis', 0)),
            ("", ""),
            ("Security Findings", ""),
            ("Vulnerabilities", len(data.get('vulnerabilities', []))),
            ("Sensitive Data", len(data.get('sensitive_data', []))),
        ]
        
        for row_idx, (label, value) in enumerate(rows, 1):
            ws.cell(row=row_idx, column=1, value=label)
            ws.cell(row=row_idx, column=2, value=value)
    
    def _create_apis_sheet(self, wb, data: Dict):
        """创建 API 列表 Sheet"""
        ws = wb.create_sheet("APIs")
        
        headers = ["Path", "Method", "Status", "Score", "Service", "Tags"]
        
        for col, header in enumerate(headers, 1):
            cell = ws.cell(row=1, column=col, value=header)
            cell.font = self.header_font
            cell.fill = self.header_fill
        
        apis = data.get('api_endpoints', [])
        for row_idx, api in enumerate(apis, 2):
            ws.cell(row=row_idx, column=1, value=api.get('path', ''))
            ws.cell(row=row_idx, column=2, value=api.get('method', 'GET'))
            ws.cell(row=row_idx, column=3, value=api.get('status', ''))
            ws.cell(row=row_idx, column=4, value=api.get('score', 0))
            ws.cell(row=row_idx, column=5, value=api.get('service_key', ''))
            ws.cell(row=row_idx, column=6, value=', '.join(api.get('tags', [])))
    
    def _create_vulns_sheet(self, wb, data: Dict):
        """创建漏洞 Sheet"""
        ws = wb.create_sheet("Vulnerabilities")
        
        headers = ["Type", "Severity", "API", "Evidence", "Remediation"]
        
        for col, header in enumerate(headers, 1):
            cell = ws.cell(row=1, column=col, value=header)
            cell.font = self.header_font
            cell.fill = self.header_fill
        
        vulns = data.get('vulnerabilities', [])
        for row_idx, vuln in enumerate(vulns, 2):
            ws.cell(row=row_idx, column=1, value=vuln.get('vuln_type', ''))
            ws.cell(row=row_idx, column=2, value=vuln.get('severity', ''))
            ws.cell(row=row_idx, column=3, value=vuln.get('api_id', ''))
            ws.cell(row=row_idx, column=4, value=vuln.get('evidence', '')[:100])
            ws.cell(row=row_idx, column=5, value=vuln.get('remediation', '')[:100])
    
    def _create_sensitive_sheet(self, wb, data: Dict):
        """创建敏感信息 Sheet"""
        ws = wb.create_sheet("Sensitive Data")
        
        headers = ["Type", "Severity", "Location", "Context"]
        
        for col, header in enumerate(headers, 1):
            cell = ws.cell(row=1, column=col, value=header)
            cell.font = self.header_font
            cell.fill = self.header_fill
        
        sensitive = data.get('sensitive_data', [])
        for row_idx, item in enumerate(sensitive, 2):
            ws.cell(row=row_idx, column=1, value=item.get('data_type', ''))
            ws.cell(row=row_idx, column=2, value=item.get('severity', ''))
            ws.cell(row=row_idx, column=3, value=item.get('location', ''))
            ws.cell(row=row_idx, column=4, value=item.get('context', '')[:100])


